    //metodo para cadastrar uma OS
    private void emitir_os() {
        String sql = "insert into tbos(tipo,situacao,servico,valor,observacao,idcli) values (?,?,?,?,?,?)";
        try {
            pst = conexao.prepareStatement(sql);    
            pst.setString(1, tipo);
            pst.setString(2, cboOsSit.getSelectedItem().toString());
            pst.setString(3, txtOsServ.getText());
            //.replace substitui a virgula pelo ponto
            pst.setString(4, txtOsValor.getText().replace(",", "."));
            pst.setString(5, txtOsObservacao.getText());
            pst.setString(6, txtCliId.getText());

            //validacao dos campos obrigatorios
            if ((txtCliId.getText().isEmpty()) || (txtOsServ.getText().isEmpty())||cboOsSit.getSelectedItem().equals(" ")) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios");
            } else {
                int adicionado = pst.executeUpdate();
                if (adicionado > 0) {
                    JOptionPane.showMessageDialog(null, "Ordem de Serviço emitida com sucesso");
                    //recuperar o numero da os
                    recuperarOs();
                    btnOsAdicionar.setEnabled(false);
                    btnOsPesquisar.setEnabled(false);
                    btnOsImprimir.setEnabled(true);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    //metodo para pesquisar uma OS
    private void pesquisar_os() {
        //a linha abaixo cria uma caixa de entrada do tipo JOption Pane
        String num_os = JOptionPane.showInputDialog("Número da Ordem de Serviço");
        String sql = "select os,date_format(data_os,'%d/%m/%Y - %H:%i'), tipo, situacao, servico, valor, observacao, idcli from tbos where os= " + num_os;
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {
                txtOs.setText(rs.getString(1));
                txtData.setText(rs.getString(2));
                //setando os radio buttons
                String rbtTipo = rs.getString(3);
                if (rbtTipo.equals("OS")) {
                    rbtOs.setSelected(true);
                    tipo = "OS";
                } else {
                    rbtOrc.setSelected(true);
                    tipo = "Orçamento";
                }
                cboOsSit.setSelectedItem(rs.getString(4));
                txtOsServ.setText(rs.getString(5));
                txtOsValor.setText(rs.getString(6));
                txtOsObservacao.setText(rs.getString(7));
                txtCliId.setText(rs.getString(8));
                //evitando problemas
                btnOsAdicionar.setEnabled(false);
                btnOsPesquisar.setEnabled(false);
                txtCliPesquisar.setEnabled(false);
                tblClientes.setVisible(false);
                //ativar demais botoes
                btnOsAlterar.setEnabled(true);
                btnOsExcluir.setEnabled(true);
                btnOsImprimir.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "Ordem de Serviço não cadastrada");
            }
        } catch (java.sql.SQLSyntaxErrorException e) {
            JOptionPane.showMessageDialog(null, "Ordem de Serviço inválida");
            //System.out.println(e);
        } catch (Exception e2) {
            JOptionPane.showMessageDialog(null, e2);
        }
    }

    //metodo para alterar uma OS
    private void alterar_os() {
        String sql = "update tbos set tipo=?,situacao=?,servico=?,valor=?,observacao=? where os=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, tipo);
            pst.setString(2, cboOsSit.getSelectedItem().toString());
            pst.setString(3, txtOsServ.getText());
            //.replace substitui a virgula pelo ponto
            pst.setString(4, txtOsValor.getText().replace(",", "."));
            pst.setString(5, txtOsObservacao.getText());
            pst.setString(6, txtOs.getText());

            //validacao dos campos obrigatorios
            if ((txtCliId.getText().isEmpty()) || (txtOsServ.getText().isEmpty())||cboOsSit.getSelectedItem().equals(" ")   ) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios");
            } else {
                int adicionado = pst.executeUpdate();
                if (adicionado > 0) {
                    JOptionPane.showMessageDialog(null, "Ordem de Serviço alterada com sucesso");
                    limpar();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    //metodo para excluir uma os
    private void excluir_os() {
        int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir essa Ordem de Serviço?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            String sql = "delete from tbos where os=?";
            try {
                pst = conexao.prepareStatement(sql);
                pst.setString(1, txtOs.getText());
                int apagado = pst.executeUpdate();
                if (apagado > 0) {
                    JOptionPane.showMessageDialog(null, "Ordem de serviço excluída com sucesso");
                    limpar();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
    
    //metodo para imprimir uma os
    private void imprimir_os(){
        //imprimindo uma os
        int confirma=JOptionPane.showConfirmDialog(null, "Confirma a impressão dessa ordem de serviço?", "Atenção",JOptionPane.YES_NO_OPTION);
        if(confirma==JOptionPane.YES_OPTION){
            //Emitindo relatorio com o framework jasperreport
            try {
                //usando a classe hashmap para criar um filtro
                HashMap filtro = new HashMap();
                filtro.put("os",Integer.parseInt(txtOs.getText()));
                //Usando a classe jasperPrint para preparar a impressao do relatorio
                JasperPrint print = JasperFillManager.fillReport("C:\\reports\\os.jasper",filtro,conexao);
                //a linha abaixo exibe o relatorio atraves da classe jasperviewer
                JasperViewer.viewReport(print,false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
//recuperar OS
    private void recuperarOs(){
        String sql = "select max(os) from tbos";
        try {
            pst=conexao.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                txtOs.setText(rs.getString(1));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
